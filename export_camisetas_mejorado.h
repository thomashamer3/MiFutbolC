/**
 * @file export_camisetas_mejorado.h
 * @brief Declaraciones de funciones para exportar camisetas mejoradas
 *
 * Este archivo contiene las declaraciones de funciones para exportar datos de camisetas
 * con estadísticas avanzadas y análisis de rendimiento en diferentes formatos.
 */

/**
 * @brief Exporta datos de camisetas a formato CSV con análisis avanzado
 *
 * Exporta los datos de camisetas incluyendo estadísticas avanzadas como
 * eficiencia de goles, asistencias, porcentaje de victorias y análisis de lesiones.
 */
void exportar_camisetas_csv_mejorado();

/**
 * @brief Exporta datos de camisetas a formato TXT con análisis avanzado
 *
 * Exporta los datos de camisetas en formato de texto con estadísticas avanzadas
 * y análisis de rendimiento en un formato legible.
 */
void exportar_camisetas_txt_mejorado();

/**
 * @brief Exporta datos de camisetas a formato JSON con análisis avanzado
 *
 * Exporta los datos de camisetas en formato JSON con estadísticas avanzadas
 * y análisis de rendimiento para fácil procesamiento por otras aplicaciones.
 */
void exportar_camisetas_json_mejorado();

/**
 * @brief Exporta datos de camisetas a formato HTML con análisis avanzado
 *
 * Exporta los datos de camisetas en formato HTML con estadísticas avanzadas
 * y análisis de rendimiento para visualización en navegadores web.
 */
void exportar_camisetas_html_mejorado();
