/**
 * @file export_all_mejorado.h
 * @brief Declaraciones de funciones para exportar datos mejorados
 *
 * Este archivo contiene las declaraciones de funciones para exportar datos
 * con análisis avanzado y estadísticas mejoradas.
 */

/**
 * @brief Muestra el menú para exportar datos mejorados
 *
 * Esta función muestra un menú interactivo que permite al usuario
 * seleccionar qué tipo de datos mejorados desea exportar.
 */
void menu_exportar_mejorado();

/**
 * @brief Exporta todos los datos de camisetas con análisis avanzado
 *
 * Exporta los datos de camisetas en todos los formatos disponibles
 * incluyendo estadísticas avanzadas y análisis de rendimiento.
 */
void exportar_camisetas_todo_mejorado();

/**
 * @brief Exporta todos los datos de lesiones con análisis avanzado
 *
 * Exporta los datos de lesiones en todos los formatos disponibles
 * incluyendo estadísticas avanzadas y análisis de impacto.
 */
void exportar_lesiones_todo_mejorado();

/**
 * @brief Exporta todos los datos con análisis avanzado
 *
 * Exporta todos los datos (camisetas y lesiones) en todos los formatos
 * disponibles, incluyendo tanto los datos mejorados como los originales
 * para compatibilidad.
 */
void exportar_todo_mejorado();
